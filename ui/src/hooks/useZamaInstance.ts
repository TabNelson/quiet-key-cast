import { useState, useEffect, useCallback } from 'react';
import { useAccount } from 'wagmi';
import { initializeFHEVM, encryptInput, resetFHEVMInstance, decryptEuint32 } from '../lib/fhevm';
import type { FhevmInstance } from '@zama-fhe/relayer-sdk/bundle';
import { BrowserProvider } from 'ethers';

export function useZamaInstance() {
  const { chainId } = useAccount();
  const [instance, setInstance] = useState<FhevmInstance | null>(null);
  const [error, setError] = useState<Error | null>(null);
  const [isLoading, setIsLoading] = useState(true);

  // Initialize FHEVM
  useEffect(() => {
    let mounted = true;

    const init = async () => {
      console.log('[useZamaInstance] Init called, chainId:', chainId);
      setIsLoading(true);
      
      if (!chainId) {
        console.log('[useZamaInstance] No chainId, skipping initialization');
        setIsLoading(false);
        return;
      }

      // Only support local network and Sepolia
      if (chainId !== 31337 && chainId !== 11155111) {
        console.error('[useZamaInstance] Unsupported network:', chainId);
        setError(new Error(`Unsupported network. Please switch to local network (31337) or Sepolia (11155111).`));
        setIsLoading(false);
        return;
      }

      try {
        setError(null);

        console.log('[useZamaInstance] Starting FHEVM initialization, chainId:', chainId);
        
        const fhevmInstance = await initializeFHEVM(chainId);

        if (mounted) {
          setInstance(fhevmInstance);
          setIsLoading(false);
          console.log('[useZamaInstance] ✅ FHEVM initialized successfully');
        }
      } catch (err: any) {
        console.error('[useZamaInstance] ❌ FHEVM initialization failed:', err);
        if (mounted) {
          setError(err);
          setIsLoading(false);
        }
      }
    };

    init();

    return () => {
      console.log('[useZamaInstance] Cleanup, chainId:', chainId);
      mounted = false;
    };
  }, [chainId]);

  // Reset instance on network change
  useEffect(() => {
    return () => {
      resetFHEVMInstance();
    };
  }, [chainId]);

  // Encryption function
  const encrypt = useCallback(
    async (contractAddress: string, userAddress: string, voteValue: number) => {
      if (!instance) {
        throw new Error('FHEVM instance not initialized');
      }
      return encryptInput(instance, contractAddress, userAddress, voteValue);
    },
    [instance]
  );

  // Decryption function
  const decrypt = useCallback(
    async (handle: string, contractAddress: string, userAddress: string) => {
      if (!instance) {
        throw new Error('FHEVM instance not initialized');
      }
      
      // Get signer from window.ethereum
      const provider = new BrowserProvider((window as any).ethereum);
      const signer = await provider.getSigner();
      
      return decryptEuint32(instance, handle, contractAddress, userAddress, signer, chainId);
    },
    [instance, chainId]
  );

  return {
    zamaInstance: instance,
    error,
    isLoading,
    isReady: !!instance && !isLoading,
    encrypt,
    decrypt,
  };
}

